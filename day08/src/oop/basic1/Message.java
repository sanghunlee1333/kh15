package oop.basic1;

// 자바에게 메세지(Message)의 구조를 알려주기 위해 생성한 도구
// 사용자가 만들었다고 해서 사용자 정의 자료형이라고 부른다
public class Message {
	//멤버 필드(field)
	String sender; //메세지에는 sender라는 문자열 구성요소가 있어야 해요!
	String content; //메세지에는 content라는 문자열 구성요소가 있어야 해요!
	String time; //메세지에는 time라는 문자열 구성요소가 있어야 해요!
	int remain; //메세지에는 remain라는 정수 구성요소가 있어야 해요!
}
