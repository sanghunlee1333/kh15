//비밀번호 ^(?=.*?[A-Z])(?=.*?[a-z])(?=.*?[0-9])(?=.*?[!@#$])[A-Za-z0-9!@#$]{8,16}$
//닉네임  ^[가-힣][가-힣0-9]{1,9}$
