public class Test01전화번호검사 {

	public static void main(String[] args) {

		String phone = "01012345678";
		
		//String regex = "^010[123456789][0123456789][0123456789][0123456789][0123456789][0123456789][0123456789][0123456789]$"; //^010[1-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9]$
		//String regex = "^010[1-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9]$";
		String regex = "^010[1-9][0-9]{7}$";
		//^ = 시작, $ = 종료, [] = one of, 0-9 = 0 ~ 9
		//{5} = x 5, {6,8} = 6 ~ 8, {10,} = 10 ~ 
		
		//phone이 regex의 패턴에 부합하는지 검사한 뒤 출력
		System.out.println(phone.matches(regex));
			
	}

}
